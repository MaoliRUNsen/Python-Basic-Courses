#算数运算符

numOne = 10
numTwo = 20


print("numOne + numTwo = %d" %(numOne + numTwo) )
print("numOne - numTwo = %d" %(numOne - numTwo) )

print("numOne * numTwo = %d" %(numOne * numTwo) )
print("numOne / numTwo = %d" %(numOne / numTwo) )

print("numOne % numTwo = {0}".format(numOne % numTwo ) )
print("numOne ** numTwo = %d" %(numOne ** numTwo))
print("numTwo  // numOne = %d" %(numTwo // numOne) )

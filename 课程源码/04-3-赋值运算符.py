#赋值运算符

result = 10

print(result)


num1 = 10 
num2 = 20

num1 = num2
print("num1 = num2 ---> {0} ".format(num1))

num1 += num2
print("num1 += num2 ---> {0}".format(num1))

num1 -= num2 
print("num1 -= num2 ---> {0}".format(num1))

num1 *= num2 
print("num1 *= num2 ---> {0}".format(num1))

num1 /= num2 
print("num1 /= num2 ---> {0}".format(num1))

num1 %= num2
print("num1 %= num2 ---> {0}".format(num1))

num1 **= num2
print("num1 **= num2 ---> {0}".format(num1))

num1 //= num2 
print("num1 //= num2 ---> {0}".format(num1))